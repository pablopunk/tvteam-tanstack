export function getTimezoneFlag(timezone: string): string {
  switch (true) {
    case timezone.startsWith("Europe"):
      return "🌍";
    case timezone.startsWith("America"):
      return "🌎";
    case timezone.startsWith("Asia"):
      return "🌏";
    case timezone.startsWith("Africa"):
      return "🌍";
    case timezone.startsWith("Oceania"):
      return "🇦🇺";
    case timezone.startsWith("Antarctica"):
      return "🇦🇶";
    default:
      return "🌐";
  }
}

